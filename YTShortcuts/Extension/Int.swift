import Foundation

public extension Int {
    /// Cast to string
    func toString()->String {
        return String(self)
    }
}

public extension Int32 {
    func toString()->String {
        return String(self)
    }
}

public extension Int64 {
    func toString()->String {
        return String(self)
    }
}
